package com.hc;

public class ReturnInfo {
    private int ErrNum;
    private String Message;
    public void setErrNum(int ErrNum) {
        this.ErrNum = ErrNum;
    }
    public int getErrNum() {
        return ErrNum;
    }

    public void setMessage(String Message) {
        this.Message = Message;
    }
    public String getMessage() {
        return Message;
    }
}
